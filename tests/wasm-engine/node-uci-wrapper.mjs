// This launcher is the supported Node UCI entrypoint. Node's Emscripten
// command-line stdin uses a blocking read which can starve delivery to pthread
// workers while a search is running. The proxy supplies private isready pumps
// and removes only the readyok response belonging to each private pump.

import { spawn } from 'node:child_process';
import { createInterface } from 'node:readline';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import process from 'node:process';

const directory = path.dirname(fileURLToPath(import.meta.url));
const runtime = path.join(directory, 'atomic-stockfish-nnue.js');
const child = spawn(process.execPath, [runtime, ...process.argv.slice(2)], {
  cwd: process.cwd(),
  env: process.env,
  stdio: ['pipe', 'pipe', 'pipe'],
});

const pendingReady = [];
let nextReadySequence = 1;
let pendingPumpReady = 0;
let pendingUserReady = 0;
let heartbeat;
let searching = false;

function writeCommand(line) {
  child.stdin.write(`${line}\n`);
}

function writeReady(line, kind) {
  pendingReady.push({ kind, sequence: nextReadySequence++ });
  if (kind === 'pump') pendingPumpReady += 1;
  else pendingUserReady += 1;
  writeCommand(line);
}

function stopHeartbeat() {
  searching = false;
  if (heartbeat) {
    clearInterval(heartbeat);
    heartbeat = undefined;
  }
}

function startHeartbeat() {
  searching = true;
  if (heartbeat) return;
  heartbeat = setInterval(() => {
    // Keep at most one private pump outstanding. User isready commands retain
    // their own queue entries, so their readyok can never be mistaken for one
    // generated by this launcher.
    if (searching && !child.killed && pendingPumpReady === 0) {
      writeReady('isready', 'pump');
    }
  }, 25);
  heartbeat.unref();
}

const input = createInterface({ input: process.stdin, crlfDelay: Infinity });
input.on('line', (line) => {
  const command = line.trim().toLowerCase();
  if (command === 'xboard') {
    process.stdout.write('Error (unsupported protocol): this WebAssembly artifact is UCI-only\n');
    return;
  }
  if (command === 'isready') writeReady(line, 'user');
  else writeCommand(line);

  const isSearch = command === 'go' || command.startsWith('go ');
  const isPerft = /^go\s+perft(?:\s|$)/.test(command);
  if (isSearch && !isPerft) startHeartbeat();
  if (command === 'quit') stopHeartbeat();
});
input.on('close', () => {
  stopHeartbeat();
  child.stdin.end();
});

const output = createInterface({ input: child.stdout, crlfDelay: Infinity });
output.on('line', (line) => {
  if (line === 'readyok') {
    const ready = pendingReady.shift();
    if (ready?.kind === 'pump') {
      pendingPumpReady -= 1;
      return;
    }
    if (ready?.kind === 'user') pendingUserReady -= 1;
  }
  process.stdout.write(`${line}\n`);
  if (line.startsWith('bestmove ')) stopHeartbeat();
});
child.stderr.pipe(process.stderr);

child.on('error', (error) => {
  stopHeartbeat();
  console.error(error);
  process.exitCode = 1;
});
child.on('exit', (code, signal) => {
  stopHeartbeat();
  if (signal) process.kill(process.pid, signal);
  else process.exitCode = code ?? 1;
});

for (const signal of ['SIGINT', 'SIGTERM']) {
  process.on(signal, () => child.kill(signal));
}
